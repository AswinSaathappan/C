#include<stdio.h>
int main()
{
	printf("BITWISE OPERATION   RETURN VALUE\n");
    printf("   12 & 14                 %d\n",12&14);
    printf("   7 | 8                 %d\n",7|8);
    printf("   7 << 8                %d\n",7<<8);
    printf("   8 >> 2                %d\n",8>>2);
    printf("   ~9                   %d\n",~9);
    printf("   7 ^ 8                 %d\n",7^8);
}
