#include<stdio.h>
int main()
{
	int x=10,y;
	y=x++;
	printf("x %d",x);
	printf("\ny %d",y);
	int a=10,b;
	b=++a;
	printf("\na %d",a);
	printf("\nb %d",b);
	int c=10,d;
	d=c--;
	printf("\nc %d",c);
	printf("\nd %d",d);
	int e=10,f;
	f=--e;
	printf("\ne %d",e);
	printf("\nf %d",f);
	
	
}
